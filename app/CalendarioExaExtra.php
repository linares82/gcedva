<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class CalendarioExaExtra extends Model
{
	use RelationManagerTrait, GetAllDataTrait;
	use SoftDeletes;

	public function __construct(array $attributes = array())
	{
		parent::__construct($attributes);
		$this->addRelationApp(new \App\Plantel, 'razon');  // generated by relation command - Plantel,CalendarioExaExtra
		$this->addRelationApp(new \App\DuracionPeriodo, 'name');  // generated by relation command - DuracionPeriodo,CalendarioExaExtra
		$this->addRelationApp(new \App\Lectivo, 'name');  // generated by relation command - Lectivo,CalendarioExaExtra	
	}

	//Mass Assignment
	protected $fillable = ['plantel_id', 'duracion_periodo_id', 'fec_inicio', 'fec_fin', 'lectivo_id', 'usu_alta_id', 'usu_mod_id'];

	public function usu_alta()
	{
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	} // end

	public function usu_mod()
	{
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	} // end


	protected $dates = ['deleted_at'];

	// generated by relation command - Plantel,CalendarioExaExtra - start
	public function plantel()
	{
		return $this->belongsTo('App\Plantel');
	} // end

	public function lectivo()
	{
		return $this->belongsTo('App\Lectivo');
	} // end

	// generated by relation command - DuracionPeriodo,CalendarioExaExtra - start
	public function duracionPeriodo()
	{
		return $this->belongsTo('App\DuracionPeriodo');
	} // end
}
