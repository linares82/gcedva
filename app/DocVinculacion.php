<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class DocVinculacion extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\Clasificacion, 'name' );  // generated by relation command - Clasificacion,DocVinculacion
    } 

	//Mass Assignment
	protected $fillable = ['name','bnd_obligatorio','usu_alta_id','usu_mod_id','orden', 'clasificacion_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end


    protected $dates = ['deleted_at'];

	// generated by relation command - DocVinculacion,DocVinculacionVinculacion - start
	public function docVinculacionVinculacions() {
		return $this->hasMany('App\DocVinculacionVinculacion');
	}// end

	// generated by relation command - Clasificacion,DocVinculacion - start
	public function clasificacion() {
		return $this->belongsTo('App\Clasificacion');
	}// end
}