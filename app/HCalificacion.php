<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class HCalificacion extends Model
{
	use RelationManagerTrait, GetAllDataTrait;
	use SoftDeletes;

	public function __construct(array $attributes = array())
	{
		parent::__construct($attributes);
		$this->addRelationApp(new \App\Cliente, 'nombre');  // generated by relation command - Cliente,HCalificacion
		$this->addRelationApp(new \App\Calificacion, 'calificacion');  // generated by relation command - Calificacion,HCalificacion
		$this->addRelationApp(new \App\CalificacionPonderacion, 'calificacion_parcial');  // generated by relation command - CalificacionPonderacion,HCalificacion
		$this->addRelationApp(new \App\CargaPonderacion, 'name');  // generated by relation command - CargaPonderacion,HCalificacion
	}

	//Mass Assignment
	protected $fillable = ['cliente_id', 'calificacion_id', 'calificacion_ponderacion_id', 'carga_ponderacion_id', 'calificacion_parcial_anterior', 'calificacion_parcial_actual', 'usu_alta_id', 'usu_mod_id'];

	public function usu_alta()
	{
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	} // end

	public function usu_mod()
	{
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	} // end


	protected $dates = ['deleted_at'];

	// generated by relation command - Cliente,HCalificacion - start
	public function cliente()
	{
		return $this->belongsTo('App\Cliente');
	} // end

	// generated by relation command - Calificacion,HCalificacion - start
	public function calificacion()
	{
		return $this->belongsTo('App\Calificacion');
	} // end

	// generated by relation command - CalificacionPonderacion,HCalificacion - start
	public function calificacionPonderacion()
	{
		return $this->belongsTo('App\CalificacionPonderacion');
	} // end

	// generated by relation command - CargaPonderacion,HCalificacion - start
	public function cargaPonderacion()
	{
		return $this->belongsTo('App\CargaPonderacion');
	} // end
}
