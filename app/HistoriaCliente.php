<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class HistoriaCliente extends Model
{
	use RelationManagerTrait, GetAllDataTrait;
	use SoftDeletes;

	public function __construct(array $attributes = array())
	{
		parent::__construct($attributes);
		$this->addRelationApp(new \App\EventoCliente, 'name');  // generated by relation command - EventoCliente,HistoriaCliente
		$this->addRelationApp(new \App\Cliente, 'nombre');  // generated by relation command - Cliente,HistoriaCliente
	}

	//Mass Assignment
	protected $fillable = [
		'evento_cliente_id', 'descripcion', 'fecha', 'archivo', 'cliente_id', 'usu_alta_id', 'usu_mod_id', 'fec_vigencia', 'inscripcion_id',
		'st_historia_cliente_id', 'aut_serv_esc', 'aut_caja', 'aut_serv_esc_corp', 'aut_director', 'aut_caja_corp'
	];

	public function usu_alta()
	{
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	} // end

	public function usu_mod()
	{
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	} // end


	protected $dates = ['deleted_at'];

	// generated by relation command - EventoCliente,HistoriaCliente - start
	public function eventoCliente()
	{
		return $this->belongsTo('App\EventoCliente');
	} // end

	// generated by relation command - Cliente,HistoriaCliente - start
	public function cliente()
	{
		return $this->belongsTo('App\Cliente');
	} // end

	public function stHistoriaCliente()
	{
		return $this->belongsTo('App\StHistoriaCliente', 'st_historia_cliente_id');
	}

	public function autSerEsc()
	{
		return $this->belongsTo('App\StHistoriaCliente', 'aut_ser_esc');
	}

	public function autCaja()
	{
		return $this->belongsTo('App\StHistoriaCliente', 'aut_caja');
	}

	public function autDirector()
	{
		return $this->belongsTo('App\StHistoriaCliente', 'aut_director');
	}

	public function autCajaCorp()
	{
		return $this->belongsTo('App\StHistoriaCliente', 'aut_caja_corp');
	}

	public function autSerEscCorp()
	{
		return $this->belongsTo('App\StHistoriaCliente', 'aut_ser_esc_corp');
	}
}
