<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;


class TitulacionEgreso extends Model
{
    use RelationManagerTrait,GetAllDataTrait;


    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\TitulacionGrupo, 'name' );  // generated by relation command - TitulacionGrupo,TitulacionEgreso
		$this->addRelationApp( new \App\TitulacionConcepto, 'name' );  // generated by relation command - TitulacionConcepto,TitulacionEgreso
    } 

	//Mass Assignment
	protected $fillable = ['titulacion_grupo_id','titulacion_concepto_id','no_alumnos','cantidad','no_horas','costo_unitario','monto_total','fecha','complemento','observacion','usu_alta_id','usu_mod_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end

	// generated by relation command - TitulacionGrupo,TitulacionEgreso - start
	public function titulacionGrupo() {
		return $this->belongsTo('App\TitulacionGrupo');
	}// end

	// generated by relation command - TitulacionConcepto,TitulacionEgreso - start
	public function titulacionConcepto() {
		return $this->belongsTo('App\TitulacionConcepto');
	}// end


}