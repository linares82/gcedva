<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;


class TitulacionGrupo extends Model
{
    use RelationManagerTrait,GetAllDataTrait;


    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
    } 

	//Mass Assignment
	protected $fillable = ['name','fecha','usu_alta_id','usu_mod_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end

	// generated by relation command - TitulacionGrupo,Titulacion - start
	public function titulacions() {
		return $this->hasMany('App\Titulacion');
	}// end

	// generated by relation command - TitulacionGrupo,TitulacionEgreso - start
	public function titulacionEgresos() {
		return $this->hasMany('App\TitulacionEgreso');
	}// end


}